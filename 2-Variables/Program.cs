// caracteres Literals
Console.WriteLine('b');

// números literals
Console.WriteLine(123);

// números float literals
Console.WriteLine(0.25F);

// números double literals
Console.WriteLine(2.625);

// números decimales
Console.WriteLine(12.39816m);

// booleanos
Console.WriteLine(true);
Console.WriteLine(false);

// VARIABLES
/**
* string: para palabras, frases o cualquier dato alfanumérico para presentación, no cálculo
* char: para un solo carácter alfanumérico
* int: por un numero entero
* decimal: para un número con un componente fraccionario
* bool: por un valor true/false
*/

// variable string
string firstName = "Elias";
Console.WriteLine(firstName);
firstName = "Bob";
Console.WriteLine(firstName);

// variable var

var message = "Hello world!";
Console.WriteLine(message);

// Ejercico de variables
string name = "Bob";
int messages = 3;
decimal temperature = 34.4m;

Console.Write("Hello, ");
Console.Write(name);
Console.Write("! You have ");
Console.Write(messages);
Console.Write(" in your inbox. The temperature is ");
Console.Write(temperature);
Console.Write(" celsius.");

